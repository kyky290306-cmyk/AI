def tron_mang(mang_a, mang_b):
    res = []
    len_min = min(len(mang_a), len(mang_b))
    for i in range(len_min):
        res.append(mang_a[i] + mang_b[i])
    res.extend(mang_a[len_min:] if len(mang_a) > len(mang_b) else mang_b[len_min:])
    return res

