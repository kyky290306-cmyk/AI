# a. Tổng tam giác trên chéo phụ (bao gồm cả đường chéo phụ)
def tong_tam_giac_tren_cheo_phu(a):
    n = len(a)
    tong = 0
    for i in range(n):
        for j in range(n):
            if i + j <= n - 1:
                tong += a[i][j]
    return tong


# b. Chuyển âm thành dương
def chuyen_am_thanh_duong(a):
    for i in range(len(a)):
        for j in range(len(a[i])):
            if a[i][j] < 0:
                a[i][j] = abs(a[i][j])
    return a


# c. Thay số chẵn bằng x
def thay_chan_bang_x(a, x):
    for i in range(len(a)):
        for j in range(len(a[i])):
            if a[i][j] % 2 == 0:
                a[i][j] = x
    return a


# d. Kiểm tra toàn chẵn
def toan_chan(a):
    for row in a:
        for x in row:
            if x % 2 != 0:
                return False
    return True


# e. Kiểm tra đối xứng (qua chéo chính)
def doi_xung(a):
    n = len(a)
    for i in range(n):
        for j in range(i + 1, n): # Chỉ cần kiểm tra một nửa tam giác
            if a[i][j] != a[j][i]:
                return False
    return True


# f. Chéo chính tăng dần
def cheo_chinh_tang(a):
    n = len(a)
    for i in range(n - 1):
        if a[i][i] >= a[i+1][i+1]:
            return False
    return True


# g. Các phần tử tam giác dưới chéo phụ
def tam_giac_duoi_cheo_phu(a):
    n = len(a)
    ket_qua = []
    for i in range(n):
        for j in range(n):
            if i + j >= n - 1:
                ket_qua.append(a[i][j])
    return ket_qua


# h. Chéo phụ giảm dần
def cheo_phu_giam(a):
    n = len(a)
    for i in range(n - 1):
        # Phần tử hiện tại: a[i][n-1-i]
        # Phần tử kế tiếp: a[i+1][n-2-i]
        if a[i][n - 1 - i] <= a[i + 1][n - 2 - i]:
            return False
    return True

