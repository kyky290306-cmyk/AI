# 1: TRỘN 2 MẢNG
def tron_mang(a, b):
    c = []
    min_len = min(len(a), len(b))

    # Lấy giá trị nhỏ hơn tại mỗi vị trí tương ứng
    for i in range(min_len):
        c.append(min(a[i], b[i]))

    # Ghép phần dư của mảng dài hơn vào sau
    if len(a) > len(b):
        c.extend(a[min_len:])
    else:
        c.extend(b[min_len:])

    return c


